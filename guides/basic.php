Bienvenido a la guia rapida para aprender a jugar, primero que nada dejame presentarme, soy tu asistente de iniciacion.
<br><br>
Te mostraré lo basico que debes saber para ser un gran guerrero! Visitame cada vez que tengas una duda aquí podrás encontrar soluciones!
<br><br>
Primero vamos con algo basico, y nada menos que la "Visión General", en esta sección podes observar tu personaje, tambien tu inventario como tu equipo de armas y armaduras.
<br><br>
<img src="8501/img/guide/guide1.png">
Además cuentas con visión a tus estadisticas de combate, como lo son la Fuerza, la cual aumenta tu daño, tu habilidad que aumenta tu posibilidad de critico y de golpeo,
 tambien puede observar tu agilidad, que aumenta tu posibilidad de esquivar ataques, la constituticion aumenta tu vida total, el carisma que poseas aumentará tu posibilidad de golpear doblemente al enemigo,
 y por ultimo tu inteligencia la cual bonifica la vida que puedas curar en hospitales.
<br><br>
En este cuadro tambien puedes observar tu nivel y experiencia actual, además de tu vida restante, como tambien puedes ver tu daño y armadura.
<br><br>
<img src="8501/img/guide/guide2.png">
Ten en cuenta que puede arrastrar los objetos en los inventarios, tambien cuentas con un segundo inventario para poder organizarte mejor.
<br>
Los objetos podrás recibirlos de distintas forma, por ejemplo, obtener objetos por evento, ejemplo:
<br>
<li>Evento: "Rata Feroz"</li>
<li>Objetivo: "Vence x10 <b>Rata</b>"</li>
<li>Premio: <b>Guantes</b></li>
<br>
Tambien puedes conseguir objeto al cumplir objetivos en eventos especiales, ejemplo:
<br><br>
<li>Objetivo: "Llegar a nivel 10"</li>
<li>Premio: "<b>Casco</b>"</li>
<br>
Por ultimo tambien puedes conseguir objeto con cajas especiales! Estas cajas puedes comprarlas en la sección Premium utilizando Rubies para su comprar.
<br><br>
Por otro lado en la parte superior puedes visualizar las bonificaciones de dioses, estas son acumulables y bonifican según pases de nivel o según tengas acumulaciones.
 Algunas pueden bonificar daño, armadura, vida y otras estadisticas como habilidad, agilidad, carisma o inteligencia.
